    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        String str="";
        if(this.gp<100){
            str="Mala suerte ";
        }else if(this.gp>=100 && this.gp<200){
                str="Buen trabajo !";
            }else if(this.gp>=200 && this.gp<300){
                str="Excelente trabajo!";
            }else if(this.gp>=300){
                str="Felicidades, ganaste la insignia de Trivia!";
                 new DashboardFormAbout().setVisible(true);
                 this.dispose();
                 JOptionPane.showMessageDialog(null, str);
            }
        
        lbl.setText(" Puntos : "+this.gp + " "+str);
        ProBar.setValue((int)this.gp);
    }//GEN-LAST:event_jButton1ActionPerformed

